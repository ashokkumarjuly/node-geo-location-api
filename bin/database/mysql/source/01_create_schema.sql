-- Dumping structure for table users
CREATE TABLE IF NOT EXISTS `users` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `firstName` varchar(50) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `lastName` varchar(50) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `email` varchar(100) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `password` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `uid` char(36) COLLATE utf8mb4_unicode_ci NOT NULL,
  `role` tinyint(1) DEFAULT '0',
  `phoneCode` varchar(5) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `phoneNo` varchar(12) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `profileImage` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL COMMENT 'Store only file names. Path should be predefined.',
  `address` text COLLATE utf8mb4_unicode_ci,
  `address2` text COLLATE utf8mb4_unicode_ci,
  `city` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `state` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `countryCode` varchar(3) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `zipCode` varchar(10) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `profileCompletion` tinyint(1) DEFAULT '0',
  `status` tinyint(1) DEFAULT '0' COMMENT '0 => Inactive, 1 => Active, -1 => Deleted',
  `emailVerified` tinyint(1) DEFAULT '0' COMMENT '0 => No, 1 => Yes',
  `authToken` varchar(255) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `verificationTokenExpires` datetime DEFAULT NULL,
  `timeZone` varchar(10) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `lastLoginDate` datetime DEFAULT NULL,
  `deletedAt` datetime DEFAULT NULL,
  `createdAt` datetime NOT NULL,
  `updatedAt` datetime DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uid_uidx` (`uid`),
  UNIQUE KEY `user_email_uidx` (`email`),
  KEY `users_status_idx` (`status`)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_unicode_ci;